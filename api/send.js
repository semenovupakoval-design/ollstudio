export default async function handler(req, res) {
  res.setHeader('Access-Control-Allow-Origin', '*');
  res.setHeader('Access-Control-Allow-Methods', 'POST, OPTIONS');
  res.setHeader('Access-Control-Allow-Headers', 'Content-Type');

  if (req.method === 'OPTIONS') return res.status(200).end();
  if (req.method !== 'POST') return res.status(405).json({ error: 'Method not allowed' });

  const { api_id, to, msg, from } = req.body;

  if (!api_id || !to || !msg) {
    return res.status(400).json({ error: 'Missing required fields' });
  }

  try {
    const params = new URLSearchParams({ api_id, to, msg, json: '1' });
    if (from) params.append('from', from);

    const response = await fetch(`https://sms.ru/sms/send?${params.toString()}`);
    const data = await response.json();
    return res.status(200).json(data);
  } catch (err) {
    return res.status(500).json({ error: 'SMS.ru request failed', details: err.message });
  }
}
